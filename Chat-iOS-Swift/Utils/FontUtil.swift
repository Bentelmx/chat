import UIKit

class FontUtil: NSObject {
    class func helveticaNeueLightFont(size: CGFloat) -> UIFont! {
        return UIFont(name: "HelveticaNeue-Light", size: size)
    }
    
    class func helveticaNeueUltraLightFont(size: CGFloat) -> UIFont! {
        return UIFont(name: "HelveticaNeue-UltraLight", size: size)
    }
}
